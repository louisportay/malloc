#include "bridge.h"

void	lswap(long *a, long *b)
{
	long c;

	c = *a;
	*a = *b;
	*b = c;
}

void	iswap(int *a, int *b)
{
	int c;

	c = *a;
	*a = *b;
	*b = c;
}
